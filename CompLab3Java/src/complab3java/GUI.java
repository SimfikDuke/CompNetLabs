    private void startServer(){
        try {
            try {
                iterator = 0;
                interval = 1000000;
                server = new ServerSocket(serverPort,1000,InetAddress.getByName(serverAddrString));
                jTextPane1.setText(jTextPane1.getText()+"Сервер "+serverAddrString+":"+serverPort+" запущен!\n");
                jTextPane1.setText(jTextPane1.getText()+"Сервер ожидает подключения клиента к порту "+serverPort+"\n");
                while(serverStarted){
                    Socket newClient = server.accept();
                    serverGUI c = new serverGUI(newClient, ++iterator, interval,jTextPane1);
                    c.start();
                }
            }  finally {
                if(!server.isClosed())server.close();
            }
        }   catch (IOException ex) {
            System.err.println("Ошибка: "+ex);
        }
    }
    
