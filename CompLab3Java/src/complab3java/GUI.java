    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(serverStarted){
            serverStarted = false;
            jButton1.setBackground(java.awt.Color.GREEN);
            try {if(!server.isClosed())server.close();} catch (IOException ex){}
            jTextPane1.setText(jTextPane1.getText()+"Сервер завершен!\n\n");
            jButton1.setText("Запустить сервер");
        }
        else{
            serverStarted = true;
            jButton1.setBackground(java.awt.Color.RED);
            jButton1.setText("Остановить сервер");
            serverAddrString = jTextField1.getText();
            try {
                serverPort = Integer.valueOf(jTextField2.getText());
                if (serverPort < 1 || serverPort > 64767) {
                    serverPort = 25562;
                    jTextField2.setText("25562");
                }
            } catch (Exception e) {
                serverPort = 25562;
                jTextField2.setText("25562");
            }
            Thread t = new Thread(new Runnable(){
            public void run(){startServer(); }});
            t.start();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

